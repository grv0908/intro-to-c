// This programs tell about size of each data types
#include <stdio.h>

int main()
{
    int a;
    char b;
    float c;
    double d;
    
    printf("Size of int is : %ld\n", sizeof(a));
    printf("Size of char is : %ld\n", sizeof(b));
    printf("Size of float is : %ld\n", sizeof(c));
    printf("Size of double is : %ld", sizeof(d));
    return 0;
}
